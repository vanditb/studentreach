function slugify(input: string) {
  return input.toLowerCase().replace(/[^a-z0-9]+/g, "-");
}

function makePapers(seed: Seed): Paper[] {
  const voice = fieldVoices[seed.field];
  const baseYear = 2026;
  return seed.tags.map((tag, index) => ({
    id: `${seed.slug}-paper-${index + 1}`,
    title: `${tag.replace(/\b\w/g, (letter) => letter.toUpperCase())} for ${seed.focus}`,
    venue: voice.venue,
    year: baseYear - index,
    summary: `Explores ${tag} through the lens of ${seed.focus}.`,
  }));
}

function makeProfessor(seed: Seed, index: number): Professor {
  const voice = fieldVoices[seed.field];
  const recentPapers = makePapers(seed);
  const title = titleRotation[index % titleRotation.length];
  const nameSlug = slugify(seed.name);

  return {
    id: seed.slug,
    name: seed.name,
    title,
    university: seed.university,
    department: seed.department,
    field: seed.field,
    city: seed.city,
    state: seed.state,
    latitude: seed.latitude,
    longitude: seed.longitude,
    facultyPage: `https://www.${slugify(seed.university)}.edu/faculty/${nameSlug}`,
    email: `${seed.name.split(" ")[0].toLowerCase()}.${seed.name.split(" ")[1].toLowerCase()}@${slugify(
      seed.university,
    )}.edu`,
    researchTags: [...seed.tags],
    currentFocus: seed.focus,
    whyFit: seed.fitAngle,
    workSummary: `${voice.summaryLead} Right now, ${seed.name} is especially interested in ${seed.focus}.`,
    researchBreakdown: [
      {
        label: voice.breakdownLabels[0],
        value: `A central theme in ${seed.name}'s work is ${seed.tags[0]}.`,
      },
      {
        label: voice.breakdownLabels[1],
        value: `Their group often combines ${seed.tags[1]} with ${seed.tags[2]} to answer more specific questions.`,
      },
      {
        label: voice.breakdownLabels[2],
        value: `The broader payoff is ${seed.fitAngle.charAt(0).toLowerCase()}${seed.fitAngle.slice(1)}`,
      },
    ],
    goodTalkingPoints: [
      voice.talkingPointLead,
      `Ask about how the lab currently thinks about ${seed.tags[0]} or ${seed.tags[1]}.`,
      `If you are new to the field, mention one reading or project that made ${seed.focus} feel approachable.`,
    ],
    recentPapers,
    recentPublicationCount: 16 + (index % 5) * 4,
    credibilitySignal: `${16 + (index % 5) * 4} recent publications and active lab updates`,
    summaryReady: index % 2 === 0,
  };
}

